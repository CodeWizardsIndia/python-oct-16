import turtle as t
def option(opt,x,y):
	t.color("blue")
	t.penup()
	t.setpos(x,y)
	t.pendown()
	t.begin_fill()
	t.lt(60)
	t.fd(50)
	t.lt(60)
	t.fd(50)
	t.lt(60)
	t.fd(150)
	t.lt(60)
	t.fd(50)
	t.lt(60)
	t.fd(50)
	t.lt(60)
	t.fd(150)
	t.end_fill()
	t.color("green")
	t.penup()
	t.setpos(x-160,y+20)
	t.pendown()
	t.write(opt,False,font=("Arial",30,"bold"))
w=t.window_width()/2
h=t.window_height()/2
option("iOS",h-10,w-50)
option("linux",h+390,w-50)
option("Windows",h-10,w-150)
option("Android",h+390,w-150)
